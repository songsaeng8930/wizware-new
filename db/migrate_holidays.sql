-- 공휴일 테이블 + 2025-2027 한국 법정 공휴일 시드
-- 2026-06-21
-- 연차 신청 시 공휴일+주말 자동 제외에 사용

CREATE TABLE IF NOT EXISTS holidays (
    id INT AUTO_INCREMENT PRIMARY KEY,
    year SMALLINT NOT NULL,
    holiday_date DATE NOT NULL,
    name VARCHAR(50) NOT NULL COMMENT '공휴일 명칭',
    type ENUM('법정','대체','임시') NOT NULL DEFAULT '법정',
    created_by INT NULL COMMENT 'employees.id (임시공휴일 등록자)',
    created_at DATETIME DEFAULT CURRENT_TIMESTAMP,
    UNIQUE KEY uq_date (holiday_date),
    INDEX idx_year (year)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='공휴일';

