-- ============================================================
-- 통합 캘린더 이벤트 (세무/4대보험/노무/회사행사)
-- ============================================================

CREATE TABLE IF NOT EXISTS calendar_events (
    id          INT AUTO_INCREMENT PRIMARY KEY,
    title       VARCHAR(200) NOT NULL,
    description TEXT NULL,
    event_date  DATE NOT NULL,
    end_date    DATE NULL COMMENT 'NULL=단일일, 기간 이벤트 시 종료일',
    category    ENUM('tax','insurance','labor','company') NOT NULL,
    is_system   TINYINT(1) NOT NULL DEFAULT 1 COMMENT '시드=1, 사용자등록=0',
    is_deadline TINYINT(1) NOT NULL DEFAULT 0 COMMENT '마감일 여부 (강조 표시)',
    source_ref  VARCHAR(200) NULL COMMENT '법적 근거',
    created_by  INT NULL,
    created_at  DATETIME DEFAULT CURRENT_TIMESTAMP,
    updated_at  DATETIME DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
    INDEX idx_date (event_date),
    INDEX idx_category (category),
    INDEX idx_year_cat (event_date, category)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_general_ci
  COMMENT='시스템 캘린더 이벤트 (세무/보험/노무/회사)';

