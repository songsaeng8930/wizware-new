-- 사내게시판 스키마
-- 실행: mysql -u root zaemit_groupware < db/schema_board.sql

CREATE TABLE IF NOT EXISTS board_posts (
    id INT AUTO_INCREMENT PRIMARY KEY,
    board_type VARCHAR(20) NOT NULL COMMENT 'notice/free/archive/department',
    category VARCHAR(30) NOT NULL COMMENT '카테고리 (공지/안내/중요/일반 등)',
    title VARCHAR(300) NOT NULL,
    content TEXT NOT NULL,
    author_id INT NULL COMMENT 'employees.id',
    author_name VARCHAR(50) NOT NULL,
    author_dept VARCHAR(100) NULL,
    department_id INT NULL COMMENT '부서게시판용 departments.id',
    is_pinned TINYINT(1) DEFAULT 0,
    views INT DEFAULT 0,
    status ENUM('active','deleted') DEFAULT 'active',
    created_at DATETIME DEFAULT CURRENT_TIMESTAMP,
    updated_at DATETIME DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
    KEY idx_type_status (board_type, status),
    KEY idx_type_pinned (board_type, is_pinned),
    KEY idx_created (created_at)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_general_ci;

